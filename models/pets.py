class Pets:
    def __init__ (self, pet_id, user_id, name, gender, date_of_birth, breed, species, weight, size, spayed_neutered):
        self.pet_id = pet_id
        self.user_id = user_id
        self.name = name
        self.gender = gender
        self.date_of_birth = date_of_birth
        self.breed = breed
        self.species = species
        self.weight = weight
        self.size = size
        self.spayed_neutered = spayed_neutered